//    validate input
    public boolean validateInput(){
        if(txtID.getText().isEmpty() || txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty()){
            return false;
        }
        return true;
    }
    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
        if(!validateInput()){
            JOptionPane.showMessageDialog(this, "Information account cannot empty");
        } else {
            Account acc = new Account();
            
            acc.setId(Integer.parseInt(txtID.getText()));
            acc.setUsername(txtUsername.getText());
            acc.setPassword(txtPassword.getText());
            
            AccountDAO accDAO = new AccountDAO();
            boolean kq = accDAO.InsertAccount(acc);
            if(kq){
                JOptionPane.showMessageDialog(this, "Register success");
                this.dispose();
                new LoginForm().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Register fail");
            }
        }
    }//GEN-LAST:event_SignUpBtnActionPerformed

